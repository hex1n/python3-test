# 将数字类型转换成字符串
print(type(666))
str_ = str(666)
print(type(str_),str_)
flat_str = str(11.134)
print(type(flat_str), flat_str)
# 将字符串转换成数字
num = int("777")
print(type(num), num)

num2 = float("11.345")
print(type(num2), num2)

# 整数转浮点数
float_num = float(11)
print(type(float_num), float_num)

# 浮点数转整数
int_num = int(11.345)
print(type(int_num), int_num)