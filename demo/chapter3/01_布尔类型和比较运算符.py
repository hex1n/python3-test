"""
演示布尔类型的定义
比较运算符的应用
"""

# 定义变量存储布尔类型的数据
bool_1 = True
bool_2 = False
print(f"bool_1变量的内容是:{bool_1},类型是:{type(bool_1)}")
print(f"bool_2变量的内容是:{bool_2},类型是:{type(bool_2)}")

# 比较运算符
# ==, != ,> , < , >= , <=

num1 = 10
num2 = 10
print(f"10==10的结果是:{num1 == num2}")

num1 = 10
num2 = 15
print(f"10!=15的结果是:{num1 != num2}")

name1 = "hello"
name2 = "hi"
print(f"hello==hi 结果是:{name1 == name2}")




